package org.palomafp.apijuegos.api.modelo.enums;

/**
 * Enumeracion que representa el estado de un videojuego en la lista
 * @author Andrés López de la Vía
 */
public enum Estado {
    /**
     * El juego no se ha empezado todavia
     */
    noEmpezado,
    /**
     * El juego esta en progreso
     */
    empezado,
    /**
     * El juego ha sido terminado
     */
    terminado
}
